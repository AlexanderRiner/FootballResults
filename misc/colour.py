# ANSI escape sequences for text formatting and colors in terminal output
# These sequences are used to change the text color, style, and reset it back to normal
# When the misc package is imported using from misc import * these can be used by simply typing the constant name

PURPLE = '\033[95m'
CYAN = '\033[96m'
DARKCYAN = '\033[36m'
BLUE = '\033[94m'
GREEN = '\033[92m'
YELLOW = '\033[93m'
RED = '\033[91m'
BOLD = '\033[1m'
UNDERLINE = '\033[4m'
END = '\033[0m'
